public class PlaceItem {
    private String placeName;
    private String placeDirection;
    private String placeLogoUrl;

    public String getPlaceName() {
        return placeName;
    }

    public void setPlaceName(String placeName) {
        this.placeName = placeName;
    }

    public String getPlaceDirection() {
        return placeDirection;
    }

    public void setPlaceDirection(String placeDirection) {
        this.placeDirection = placeDirection;
    }

    public String getPlaceLogoUrl() {
        return placeLogoUrl;
    }

    public void setPlaceLogoUrl(String placeLogoUrl) {
        this.placeLogoUrl = placeLogoUrl;
    }
}
